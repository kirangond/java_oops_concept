package com.statements;

public class Switch {

	public static void main(String[] args) {
		int key = 1;
		switch (key) {
		case 1:
			System.out.println("print 100");
			break;
		case 2:
			System.out.println("print 200");
			break;
		case 3:
			System.out.println("print 300");
			break;
		default:
			System.out.println("print 400");
			break;
		}

	}

}
